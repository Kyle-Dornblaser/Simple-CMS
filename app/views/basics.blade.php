<h3>History</h3>
<p>
	We might live in a world where everybody knows what a webpage is, but the history of webpages doesn&#39;t go back that far.
	And, like most things, it started with a really smart person who figured out how to do something they really wanted to do. Here&#39;s
	another excerpt from Wikipedia:
</p>
<blockquote cite="http://en.wikipedia.org/wiki/Web_design">
	In 1989, whilst working at CERN Tim Berners-Lee proposed to create a global hypertext project, which later
	became known as the World Wide Web. Throughout 1991 to 1993 the World Wide Web was born. Text only pages could be viewed
	using a simple line-mode browser. In 1993 Marc Andreessen and Eric Bina, created the Mosaic browser. At the time there
	were multiple browsers, however, the majority of them were Unix-based and were naturally text heavy. There had been no
	integrated approach to graphical design elements such as images or sounds. The Mosaic browser broke this mould. The
	W3C was created in October 1994, to "lead the World Wide Web to its full potential by developing common protocols that
	promote its evolution and ensure its interoperability." This discouraged any one company from monopolizing a propriety
	browser and programming language, which could have altered the effect of the World Wide Web as a whole. The W3C continues
	to set standards, which can today be seen with JavaScript. In 1994 Andreessen formed Communications corp. That later
	became known as Netscape Communications-the Netscape 0.9 browser. Netscape created its own HTML tags without regards to
	the traditional standards process. For example Netscape 1.1 included tags for changing background colours and formatting
	text with tables on web pages. Throughout 1996 to 1999 the browser wars began, as Microsoft and Netscape fought for ultimate
	browser dominance. During this time there were many new technologies in the field, notably Cascading Style Sheets, JavaScript,
	and Dynamic HTML. On a whole the browser competition did lead to many positive creations and helped web design evolve at a
	rapid pace.
</blockquote>
